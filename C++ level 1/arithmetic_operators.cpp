#include <iostream>
using namespace std;

int main()
{
    // Homework 1:
    
    int x, y;
    cout << "Please enter the first number: ";
    cin >> x;

    cout << "Please enter the second number: ";
    cin >> y;

    cout << x << " + " << y << " = " << x + y << endl;
    cout << x << " - " << y << " = " << x - y << endl;
    cout << x << " * " << y << " = " << x * y << endl;
    cout << x << " / " << y << " = " << x / y << endl;
    cout << x << " % " << y << " = " << x % y << endl;

    return 0;
}